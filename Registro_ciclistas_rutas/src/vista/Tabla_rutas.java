    void eliminarRuta(){
        int row = tbl_rutas.getSelectedRow();
        System.out.println("fila: "+ row);
        if(row == -1)
            JOptionPane.showMessageDialog(this, "Debes de seleccionar un registro de la tabla");
        else{
            String id= tbl_rutas.getValueAt(row, 0).toString();
            
            String sql = "DELETE FROM `rutas` WHERE id = "+id;
            try{
                cn = con.getConnection();
                st = cn.createStatement();
                st.executeUpdate(sql);
                clear_rows_table();
                JOptionPane.showMessageDialog(this, "Se ha eliminado el registro");
                show_rutas();
            }catch(SQLException e){
                JOptionPane.showMessageDialog(this, "No se pudo eliminar la información del registro");
            }
        }
    }
    
    
    void show_rutas(){
        String query = "SELECT * FROM rutas";
        try{
            cn = con.getConnection();
            st = cn.createStatement();
            rs = st.executeQuery(query);
            Object[] ruta = new Object[8];
            model = (DefaultTableModel)tbl_rutas.getModel();
            while(rs.next()){
                
                ruta[0] = rs.getString("id");
                ruta[1] = rs.getString("Lugar_inicio");
                ruta[2] = rs.getString("Lugar_final");
                ruta[3] = rs.getString("Hora_inicio");
                ruta[4] = rs.getString("Hora_final");
                ruta[5] = rs.getFloat("KM");
                ruta[6] = rs.getInt("tiempo");
                ruta[7] = rs.getString("fecha");
                
                
                model.addRow(ruta);
            }
            tbl_rutas.setModel(model);
        }catch(SQLException e){
            JOptionPane.showConfirmDialog(this, "error al mostrar la tabla");
        }
    }
    
    void clear_rows_table(){
        for (int i = 0; i < tbl_rutas.getRowCount(); i++) {
            model.removeRow(i);
            i -= 1;
        }
        txt_dia.setText("");
        txt_horaFinal.setText("");
        txt_horaInicio.setText("");
        txt_km.setText("");
        txt_lugFinal.setText("");
        txt_lugInicio.setText("");
    }
    
    public int obtenerTiempo(int t1, int t2){
        int t=0;
        if(t1>t2){
            if(t1>12){
                t1=t1-12;
            }
            t=t1+t2;
        }else{
            t=t2-t1;
        }
        
        
        return t;
    }

