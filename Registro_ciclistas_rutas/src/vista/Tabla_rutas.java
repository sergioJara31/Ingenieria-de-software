    public void agregarRuta(){
        
        String lugarInicio=txt_lugInicio.getText();
        String lugFinal=txt_lugFinal.getText();
        String hi=txt_horaInicio.getText();
        String hf=txt_horaFinal.getText();
        float km=Float.valueOf(txt_km.getText());
        
        String dia= txt_dia.getText();
        
        
        if(Integer.parseInt(dia)>30 || Integer.parseInt(dia)<0){
            JOptionPane.showMessageDialog(this, "por favor ingrese una fecha que funcione");
        }else{
        int tiempo=0;
            int tiempo1=Integer.parseInt(hi);
            int tiempo2=Integer.parseInt(hf);
            if(tiempo1>24 || tiempo2>24 || tiempo1<0 || tiempo2<0){
                JOptionPane.showMessageDialog(this, "ingrese horas que coincidan");
            }else{
                tiempo= obtenerTiempo(tiempo1, tiempo2);
            }
        if (lugFinal.isEmpty() || lugarInicio.isEmpty() || txt_km.getText().isEmpty() || hi.isEmpty()|| hf.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Faltan campos por diligenciar");
        } else {
            
            System.out.println(tiempo);
            System.out.println(lugarInicio+" "+ lugFinal +" "+ hi +" "+ hf +" "+ km);
            String query = "INSERT INTO rutas(Lugar_inicio, Lugar_final, Hora_inicio, Hora_final ,KM , tiempo ,fecha ) VALUES('" + lugarInicio + "','" + lugFinal + "','" + hi + "','" + hf + "','" + km + "','" + tiempo + "','" + dia + "-09-2021')";
                    try {
                        cn = con.getConnection();
                        st = cn.createStatement();
                        st.executeUpdate(query);
                        JOptionPane.showMessageDialog(this, "Se ha creado el registro con éxito");
                        clear_rows_table();
                        show_rutas();
                    } catch (SQLException e) {
                        JOptionPane.showMessageDialog(this, "No se pudo crear el registro");
                    
                    }            
        }
        } 
    }
    
